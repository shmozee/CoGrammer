#Storing my inputs
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
num3 = int(input("Enter the third number: "))

# doing the math
sum_of_all = num1 + num2 + num3
first_minus_second = num1 - num2
third_times_first = num3 * num1
sum_all_divide_third = sum_of_all / num3

# printing the outputs
print("The Sum of all the numbers: ", sum_of_all)
print("The first number minus the second number: ", first_minus_second)
print("The third number multiplied by the first number: ", third_times_first)
print("The sum of all three numbers divided by the third number: ", sum_all_divide_third)